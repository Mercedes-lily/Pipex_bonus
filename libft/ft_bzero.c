#include <unistd.h>

void	ft_bzero(void *s, size_t n)
{
	unsigned long	i;
	char			*s2;

	i = 0;
	s2 = (char *)s;
	while (i < n)
	{
		s2[i] = 0;
		i++;
	}
}
